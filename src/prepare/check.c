# include <h_philo.h>

int check_input(char *argv[])
{
    int i;

    i = 0;
    while (argv[i] != NULL)
    {
        if (ft_isdigit(ft_atoi(argv[i])))
            return (1);
        i ++;
    }
    return (0);
}
